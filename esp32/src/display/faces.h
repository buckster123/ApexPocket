// ==================== EYE TYPES ====================

enum EyeType {
    EYE_NORMAL = 0,    // ●  - filled circle
    EYE_CLOSED,        // ─  - horizontal line (blink/sleep)
    EYE_HAPPY,         // ◡  - happy arc
    EYE_STAR,          // ★  - excited star
    EYE_WIDE,          // ◯  - surprised
    EYE_HEART,         // ♥  - love
    EYE_SLEEPY,        // ─● - half closed
    EYE_CURIOUS,       // ◉  - big pupil
    EYE_SPIRAL,        // @  - confused
    EYE_HALF_LEFT,     // ◐  - looking left (thinking)
    EYE_WINK           // ─  - single closed (right eye only)
};

// ==================== MOUTH TYPES ====================

enum MouthType {
    MOUTH_NEUTRAL = 0, // ─   - flat line
    MOUTH_SMILE,       // ◡   - happy curve
    MOUTH_BIG_SMILE,   // ╰─╯ - excited
    MOUTH_FROWN,       // ╭─╮ - sad
    MOUTH_OPEN,        // ○   - surprised
    MOUTH_SMALL_O,     // o   - curious
    MOUTH_WAVY,        // ~~~  - confused/thinking
    MOUTH_SLEEPY,      // ‿   - gentle sleep smile
    MOUTH_HUNGRY,      // □   - open square
    MOUTH_KISS         // ♥   - love (tiny heart)
};

// ==================== FACE DEFINITIONS ====================

struct FaceDefinition {
    EyeType leftEye;
    EyeType rightEye;
    MouthType mouth;
    bool hasAccessory;
    char accessory;      // '!' '?' 'z' 'Z' etc
    int8_t accessoryX;   // offset from center
    int8_t accessoryY;   // offset from top
};

